/* ─────────────────────────────────────────
   RaiseKit — generator.js
   Client-side logic for the generator page.
   Calls the /generate API, renders the plan,
   and saves via /playbooks.
───────────────────────────────────────── */

// ── CSRF helper ───────────────────────────────────────────────────────────

function getCsrfToken() {
  const meta = document.querySelector('meta[name="csrf-token"]');
  return meta ? meta.content : '';
}

function renderList(ulEl, items) {
  ulEl.innerHTML = '';
  items.forEach(item => {
    const li = document.createElement('li');
    li.textContent = item;
    ulEl.appendChild(li);
  });
}

function renderTimeline(containerEl, weeks) {
  containerEl.innerHTML = '';
  weeks.forEach(w => {
    const div = document.createElement('div');
    div.className = 'timeline-week';
    const labelEl = document.createElement('div');
    labelEl.className = 'week-label';
    labelEl.textContent = w.label;
    const tasksEl = document.createElement('div');
    tasksEl.className = 'week-tasks';
    tasksEl.textContent = w.tasks;
    div.appendChild(labelEl);
    div.appendChild(tasksEl);
    containerEl.appendChild(div);
  });
}

// ── Copy helpers ──────────────────────────────────────────────────────────

function getTextContent(el) {
  if (!el) return '';
  if (el.tagName === 'UL') {
    return Array.from(el.querySelectorAll('li')).map(li => li.textContent).join('\n');
  }
  if (el.id === 'timeline-content') {
    return Array.from(el.querySelectorAll('.timeline-week'))
      .map(w => `${w.querySelector('.week-label').textContent}: ${w.querySelector('.week-tasks').textContent}`)
      .join('\n');
  }
  return el.textContent || el.innerText;
}

function setupCopyButtons() {
  document.querySelectorAll('.copy-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const target = document.getElementById(btn.dataset.target);
      navigator.clipboard.writeText(getTextContent(target)).then(() => {
        btn.textContent = 'Copied!';
        btn.classList.add('copied');
        setTimeout(() => { btn.textContent = 'Copy'; btn.classList.remove('copied'); }, 1800);
      });
    });
  });
}

// ── Build full-plan text ──────────────────────────────────────────────────

function buildFullPlanText(plan) {
  const lines = [];
  lines.push('══════════════════════════════════════');
  lines.push('         RAISEKIT — YOUR FUNDRAISING PLAN');
  lines.push('══════════════════════════════════════\n');
  lines.push(`Org type:     ${plan.orgLabel}`);
  lines.push(`Goal:         ${plan.goalLabel}`);
  lines.push(`Timeline:     ${plan.timelineLabel}`);
  lines.push(`Priority:     ${plan.priorityLabel}`);
  if (plan.mission) lines.push(`Mission:      ${plan.mission}`);
  lines.push('');

  lines.push('── WHAT TO DO TODAY ──');
  plan.todayActions.forEach(a => lines.push('• ' + a));
  lines.push('');

  lines.push('── RECOMMENDED STRATEGY ──');
  lines.push(plan.strategySummary);
  lines.push('');

  lines.push('── ACTION TIMELINE ──');
  plan.timeline.forEach(w => lines.push(`${w.label}: ${w.tasks}`));
  lines.push('');

  lines.push('── DONOR OUTREACH TEMPLATE ──');
  lines.push(plan.donorOutreach);
  lines.push('');

  lines.push('── GRANT STARTER LIST ──');
  plan.grantIdeas.forEach(g => lines.push(g));
  lines.push('');

  lines.push('── CHECKLIST ──');
  plan.checklist.forEach(c => lines.push(c));
  lines.push('');

  lines.push('══════════════════════════════════════');
  lines.push('Generated by RaiseKit · github.com/Giancarla-Burgos/test-fundraising');
  lines.push('══════════════════════════════════════');

  return lines.join('\n');
}

function downloadPlan(plan) {
  const text = buildFullPlanText(plan);
  const blob = new Blob([text], { type: 'text/plain' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = 'raisekit-plan.txt';
  a.click();
  URL.revokeObjectURL(url);
}

function copyFullPlan(btnId, plan) {
  navigator.clipboard.writeText(buildFullPlanText(plan)).then(() => {
    const btn = document.getElementById(btnId);
    const orig = btn.textContent;
    btn.textContent = '✓ Copied!';
    setTimeout(() => { btn.textContent = orig; }, 2000);
  });
}

// ── Render plan ───────────────────────────────────────────────────────────

let _currentPlan = null;

function renderPlan(plan) {
  _currentPlan = plan;

  document.getElementById('playbook-subtitle').textContent = plan.subtitle;
  document.getElementById('fit-badge').textContent = plan.fitBadge;

  renderList(document.getElementById('today-content'), plan.todayActions);
  document.getElementById('strategy-content').textContent = plan.strategySummary;
  renderTimeline(document.getElementById('timeline-content'), plan.timeline);
  document.getElementById('email-content').textContent = plan.donorOutreach;
  renderList(document.getElementById('grants-content'), plan.grantIdeas);
  renderList(document.getElementById('checklist-content'), plan.checklist);

  document.getElementById('empty-state').classList.add('hidden');
  document.getElementById('loading-state').classList.add('hidden');
  document.getElementById('output').classList.remove('hidden');

  document.getElementById('results').scrollIntoView({ behavior: 'smooth', block: 'start' });
  setupCopyButtons();
}

// ── Save plan ─────────────────────────────────────────────────────────────

async function savePlan(plan, btnId) {
  const btn = document.getElementById(btnId);
  const statusEl = document.getElementById('save-status');
  btn.disabled = true;
  btn.textContent = 'Saving…';

  const payload = {
    orgType: plan.orgType,
    fundingGoal: plan.fundingGoal,
    timeHorizon: plan.timeHorizon,
    fundingPriority: plan.fundingPriority,
    mission: plan.mission || '',
    currentStage: plan.currentStage || '',
    strategySummary: plan.strategySummary,
    todayActions: JSON.stringify(plan.todayActions),
    timeline: JSON.stringify(plan.timeline),
    donorOutreach: plan.donorOutreach,
    grantIdeas: JSON.stringify(plan.grantIdeas),
    checklist: JSON.stringify(plan.checklist),
  };

  try {
    const resp = await fetch('/playbooks', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'X-CSRF-Token': getCsrfToken(),
      },
      body: JSON.stringify(payload),
    });
    const data = await resp.json();

    if (resp.ok && data.success) {
      const link = document.createElement('a');
      link.href = '/dashboard';
      link.textContent = 'Go to Dashboard →';
      link.style.cssText = 'color:inherit;font-weight:700;';
      statusEl.className = 'save-status';
      statusEl.textContent = '✅ Playbook saved! ';
      statusEl.classList.remove('hidden');
      statusEl.appendChild(link);
      btn.textContent = '✓ Saved';
      // Also update bottom button
      const bottomBtn = document.getElementById('save-plan-btn-bottom');
      if (bottomBtn) { bottomBtn.textContent = '✓ Saved'; bottomBtn.disabled = true; }
    } else {
      throw new Error(data.error || 'Save failed');
    }
  } catch (err) {
    statusEl.className = 'save-status error';
    statusEl.textContent = '❌ Could not save. Please try again.';
    statusEl.classList.remove('hidden');
    btn.disabled = false;
    btn.textContent = '💾 Save to Dashboard';
  }
}

// ── Event listeners ──────────────────────────────────────────────────────

document.addEventListener('DOMContentLoaded', () => {

  // Form submit
  document.getElementById('playbook-form').addEventListener('submit', async (e) => {
    e.preventDefault();
    const form = e.target;

    const emptyState = document.getElementById('empty-state');
    const loadingState = document.getElementById('loading-state');
    const output = document.getElementById('output');

    emptyState.classList.add('hidden');
    output.classList.add('hidden');
    loadingState.classList.remove('hidden');

    const formData = new FormData(form);
    const params = new URLSearchParams(formData);

    try {
      const resp = await fetch('/generate', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/x-www-form-urlencoded',
          'Accept': 'application/json',
          'X-CSRF-Token': getCsrfToken(),
        },
        body: params.toString(),
      });
      const data = await resp.json();

      if (!resp.ok || data.errors) {
        loadingState.classList.add('hidden');
        emptyState.classList.remove('hidden');
        alert(data.errors ? data.errors.join('\n') : 'Generation failed. Please try again.');
        return;
      }

      renderPlan(data.plan);
    } catch (err) {
      loadingState.classList.add('hidden');
      emptyState.classList.remove('hidden');
      alert('Something went wrong. Please try again.');
    }
  });

  // Preset buttons
  document.querySelectorAll('.preset-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const form = document.getElementById('playbook-form');
      form.orgType.value = btn.dataset.org;
      form.fundingGoal.value = btn.dataset.goal;
      form.timeHorizon.value = btn.dataset.timeline;
      form.fundingPriority.value = btn.dataset.priority;
      form.dispatchEvent(new Event('submit', { cancelable: true, bubbles: true }));
    });
  });

  // Regenerate button
  document.getElementById('regenerate-btn').addEventListener('click', () => {
    const form = document.getElementById('playbook-form');
    form.dispatchEvent(new Event('submit', { cancelable: true, bubbles: true }));
  });

  // Save buttons
  document.getElementById('save-plan-btn').addEventListener('click', () => {
    if (_currentPlan) savePlan(_currentPlan, 'save-plan-btn');
  });
  document.getElementById('save-plan-btn-bottom').addEventListener('click', () => {
    if (_currentPlan) savePlan(_currentPlan, 'save-plan-btn-bottom');
  });

  // Download buttons
  document.getElementById('download-btn-top').addEventListener('click', () => {
    if (_currentPlan) downloadPlan(_currentPlan);
  });
  document.getElementById('download-btn').addEventListener('click', () => {
    if (_currentPlan) downloadPlan(_currentPlan);
  });
});
